namespace Skabas\Baldr\Image\Attribute;

/**
 * Class Position
 * @package Skabas\Baldr
 */
class Position
{
    /** @var int $x */
    private $x;
    /** @var int $y */
    private $y;

    public function __construct(int $x, int $y)
    {
        $this->x = $x;
        $this->y = $y;
    }

    /**
     * @return int
     */
    public function getX()
    {
        return $this->x;
    }

    /**
     * @return int
     */
    public function getY()
    {
        return $this->y;
    }
}